import streamlit as st
from google import genai
from google.genai import types

# ==========================================
# 1. 画面の初期設定（スマホ専用レイアウト）
# ==========================================
st.set_page_config(page_title="AI投資信託エージェント", layout="centered") 
st.title("🤝 AI投資信託・未来発掘エージェント")

# ==========================================
# 2. セキュリティ（スマホ用ログイン画面）
# ==========================================
MY_PASSWORD = "pass" 

user_password = st.text_input("認証パスワードを入力してください", type="password")
if user_password != MY_PASSWORD:
    st.warning("正しいパスワードを入力するとアプリが起動します。")
    st.stop()

st.write("特定のファンドを指定せず、GeminiがGoogle検索を使って最新の投資信託市場（新設ファンドや最安コスト競争）を完全リアルタイムに自動リサーチ・発掘します。")

# ==========================================
# 3. 秘密金庫（Secrets）からAPIキーを安全に読み込む
# ==========================================
if "GEMINI_API_KEY" in st.secrets:
    API_KEY = st.secrets["GEMINI_API_KEY"]
else:
    st.error("WEB上の隠し金庫（Secrets）に 'GEMINI_API_KEY' が登録されていません。")
    st.stop()

# ==========================================
# 4. 画面上のボタンと実行処理
# ==========================================
if st.button("🚀 最新トレンド・新設投信の自動発掘を開始", type="primary"):
    with st.spinner("🔍 Geminiが日本の投信市場をネットパトロール中...（新設ファンドを検索しています、約1分かかります）"):
        client = genai.Client(api_key=API_KEY)
        
        # あえてファンド名を指定せず、ネット上の「新規設定」「新設」「トレンド」というキーワードでAIに自発的に探させます
        prompt = """
        あなたは資産運用と最新の投資信託（ファンド）の動向に命を懸けているプロ中のプロ、天才フィナンシャルプランナーです。
        
        【課せられたミッション】
        Google検索を駆使して、日本の公募投資信託市場における「直近（数ヶ月以内）の新設・新規設定ファンド」および「今まさにSNSやマネーニュースで話題沸騰している最新のトレンド商品」をリアルタイムに自動でリサーチ・発掘してください。
        
        あらかじめ決まった王道（オルカンや通常のS&P500のみなど）の提案は退屈なので避けてください。ユーザーが「そんな商品が新しく出ていたのか！」とワクワクするような、新たな発見ができるレポートを以下の4つの構成で日本語で詳細に執筆してください。

        1. 【実態リサーチ：今、投信市場で起きている最新トレンド】
           現在、日本の投資信託でどんなテーマ（例：特定の半導体、インド株の新しい指数、高配当インデックス、モメンタム戦略など）に資金や新商品が集まっているか、リサーチした市場の空気を解説。
        2. 【全自動発掘：近年新設された大注目のファンド3選】
           ネット検索で見つけた「直近で新規設定・新設されたばかりの新しいファンド」や「信託報酬（コスト）の常識を覆す最安値で登場した新興ファンド」を【3つ】具体的に発掘して名前を挙げ、それぞれの特徴と、なぜ今注目すべきなのかの理由。
        3. 【お宝ファンドを活かす理想のスパイス投資比率】
           これら新しく発掘したトレンドファンドを、自分の資産の「サテライト（攻めのサブ枠）」として既存の資産に何％くらい組み合わせるのが最も効果的か、具体的なポートフォリオの黄金比率の提案。
        4. 【新興・テーマ型ファンド選びの冷徹な注意点】
           新しく作られたファンドならではの「純資産が小さいうちの繰上償還リスク」や「隠れコスト」など、飛びつく前に初心者が絶対に警戒すべき落とし穴。
        """

        try:
            # Google検索を強制オンにして実行
            response = client.models.generate_content(
                model='gemini-2.5-flash',
                contents=prompt,
                config=types.GenerateContentConfig(tools=[{"google_search": {}}])
            )
            st.header("🏆 AIエージェントによる最新ファンド発掘レポート")
            st.markdown(response.text)
        except Exception as e:
            st.error(f"エラーが発生しました。時間を置いてもう一度お試しください。({e})")
