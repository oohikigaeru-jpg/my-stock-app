import streamlit as st
from google import genai
from google.genai import types

# ==========================================
# 1. 画面の初期設定（スマホ専用レイアウト）
# ==========================================
st.set_page_config(page_title="AI投資信託エージェント", layout="centered") 
st.title("🤝 AI投資信託・厳選エージェント")

# ==========================================
# 2. セキュリティ（スマホ用ログイン画面）
# ==========================================
MY_PASSWORD = "pass" 

user_password = st.text_input("認証パスワードを入力してください", type="password")
if user_password != MY_PASSWORD:
    st.warning("正しいパスワードを入力するとアプリが起動します。")
    st.stop()

st.write("新NISA対応の定番ファンドに加え、近年新設されたトレンドファンドをGeminiがGoogle検索でリアルタイムに調査し、あなたに最適な組み合わせを提案します。")

# ==========================================
# 3. 秘密金庫（Secrets）からAPIキーを安全に読み込む
# ==========================================
if "GEMINI_API_KEY" in st.secrets:
    API_KEY = st.secrets["GEMINI_API_KEY"]
else:
    st.error("WEB上の隠し金庫（Secrets）に 'GEMINI_API_KEY' が登録されていません。")
    st.stop()

# ==========================================
# 4. エージェントにリサーチさせる「最新・定番ファンド群」
# ==========================================
fund_target_list = """
【王道・定番インデックス枠】
・eMAXIS Slim 全世界株式（オール・カントリー）
・eMAXIS Slim 米国株式（S&P500）
・楽天・プラスシリーズ（楽天・オールカントリー / 楽天・S&P500）
・SBI・V・シリーズ

【近年（2024〜2026年）新設・トレンド枠】
・ニッセイ・S米国グロース株式メガ10（メガ10）
・iFreeNEXT 全世界半導体株インデックス
・iFreeNEXT スペース・テック株インデックス
・SBI・光ハイクオリティ・バリュー株ファンド
・ニッセイ NYダウ高配当株トップ10（ダウハイ10）
・FANG+インデックス関連ファンド
"""

# ==========================================
# 5. 画面上のボタンと実行処理
# ==========================================
if st.button("🚀 投資信託の自動選定・リサーチを開始", type="primary"):
    with st.spinner("🧠 GeminiがGoogle検索を使い、最新の信託報酬や資金流入をリサーチ中...（約1分かかります）"):
        client = genai.Client(api_key=API_KEY)
        
        prompt = f"""
        あなたは資産運用と投資信託（ファンド）に精通した極めて優秀なフィナンシャルプランナーです。
        以下の「定番ファンド」および「2025年〜2026年最新の新設ファンドリスト」をベースに、Google検索で現在のリアルタイムな信託報酬（コスト最安競争）、純資産の伸び、市場の評価をリサーチしてください。

        リサーチした上で、新しい発見や最適なポートフォリオが見つかるよう、以下の4つの構成で日本語で分かりやすく提案・解説してください。

        1. 【王道・定番の最適解】
           オルカンやS&P500などのインデックス枠において、現在「最もコストが安く、資金が集まっていて安全」と言えるファンドの最新状況とおすすめ理由。
        2. 【新興・トレンドお宝枠（新たな発見）】
           近年（2024〜2026年）に新設されたファンド（半導体、メガ10、スペース、高配当トップ10など）の中で、今最も勢いがあり、サテライト（サブ資産）として組み込む価値のある注目ファンドを1〜2つ厳選し、その理由。
        3. 【AIが提案する理想の組み合わせ（ポートフォリオ案）】
           初心者に向けて「王道ファンド：新興トレンドファンド」をどのような比率（％）で持てば、リスクを抑えつつ最大の果実（リターン）を狙えるか、具体的な黄金比率の提案。
        4. 【現在の投資信託選びのリスクと注意点】
           新設ファンドの繰上償還リスクや、隠れコストについて初心者が気をつけるべきポイント。

        【ターゲットリスト】
        {fund_target_list}
        """

        try:
            # Google検索を強制オンにして実行
            response = client.models.generate_content(
                model='gemini-2.5-flash',
                contents=prompt,
                config=types.GenerateContentConfig(tools=[{"google_search": {}}])
            )
            st.header("🏆 AIエージェントによる投資信託・厳選レポート")
            st.markdown(response.text)
        except Exception as e:
            st.error(f"エラーが発生しました。時間を置いてもう一度お試しください。({e})")
